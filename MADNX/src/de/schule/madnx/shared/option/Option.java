public class Option {
	private String option;
	private String state;
	
	public void setOption(String option) {
		this.option = option;
	}
	
	public void setState(String state) {
		this.state = state;
	}
	
	public String getOption() {
		return option;
	}
	
	public String getState() {
		return state;
	}
}
